#include <iostream>
using namespace std;
int main(){
	int a[4],b[4],c[4],n,A=0,B=0;
	while(scanf("%d %d %d %d",&c[0],&c[1],&c[2],&c[3])>0){
		scanf("%d",&n);
		for(;n>0;n--){
			a[0]=c[0];
			a[1]=c[1];
			a[2]=c[2];
			a[3]=c[3];
			scanf("%d",&b[0]);
			if(b[0]==a[0]){
				a[0]=-1;b[0]=-1;A++;
			} 
			scanf("%d",&b[1]);
			if(b[1]==a[1]){
				a[1]=-1;b[1]=-1;A++;
			} 
			scanf("%d",&b[2]);
			if(b[2]==a[2]){
				a[2]=-1;b[2]=-1;A++;
			} 
			scanf("%d",&b[3]);
			if(b[3]==a[3]){
				a[3]=-1;b[3]=-1;A++;
			} 
			if(A<4){
				if(b[0]!=-1){
					if(b[0]==a[1]){
						b[0]=-1;a[1]=-1;B++;
					}
					else if(b[0]==a[2]){
						b[0]=-1;a[2]=-1;B++;
					}
					else if(b[0]==a[3]){
						b[0]=-1;a[3]=-1;B++;
					}
				}
				if(b[1]!=-1){
					if(b[1]==a[0]){
						b[1]=-1;a[0]=-1;B++;
					}
					else if(b[1]==a[2]){
						b[1]=-1;a[2]=-1;B++;
					}
					else if(b[1]==a[3]){
						b[1]=-1;a[3]=-1;B++;
					}
				}
				if(b[2]!=-1){
					if(b[2]==a[1]){
						b[2]=-1;a[1]=-1;B++;
					}
					else if(b[2]==a[0]){
						b[2]=-1;a[0]=-1;B++;
					}
					else if(b[2]==a[3]){
						b[2]=-1;a[3]=-1;B++;
					}
				}
				if(b[3]!=-1){
					if(b[3]==a[1]){
						b[3]=-1;a[1]=-1;B++;
					}
					else if(b[3]==a[0]){
						b[3]=-1;a[0]=-1;B++;
					}
					else if(b[3]==a[2]){
						b[3]=-1;a[2]=-1;B++;
					}
				}
			}
			printf("%dA%dB\n",A,B);
			A=0;B=0;
		}
	}
} 
